public class DoNDrills {
    public static void main(String[] args){
        for (int i = 0; i < 10; i++){
            System.out.println(i);
        }
        for (int a = 0; a < 11; a++){
            System.out.println(a);
        }
        for (int b = 10; b >= 0; b--){
            System.out.println(b);
        }
    }
}
